//Print the sum of numbers from 1 to 100 using a for loop.

public class SumOfNo {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 100; i++) {
            sum = sum + i;

        }
        System.out.println("Sum of n ubers from 1 to 100 is : " + sum);
    }
}

// Sum of n ubers from 1 to 100 is : 5050
