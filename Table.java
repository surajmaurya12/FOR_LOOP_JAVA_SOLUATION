//Print multiplication table of a number.

public class Table {
    public static void main(String[] args) {
        int num = 5; // number for which table is to be printed
        System.out.println("Multiplication of " + num + " is: ");
        for (int i = 1; i <= 10; i++) {
            System.out.println(num + " * " + i + " = " + num * i);
        }
    }
}

